public void listar(){
        NvDificuldadeDAO dao = new NvDificuldadeDAO();
        List<NvDificuldade> lista = dao.listarNvDificuldades();
        DefaultTableModel dados = (DefaultTableModel)tbNvDificuldade.getModel();
        dados.setNumRows(0);
        
        for(NvDificuldade n: lista){
            dados.addRow(new Object[]{
           n.getCodDificuldade(),
                n.getQdtDificuldade(),
            n.getTextoDifuculdade()
        });
        }
    }
