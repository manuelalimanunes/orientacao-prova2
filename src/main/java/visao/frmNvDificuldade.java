    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        NvDificuldade obj = new NvDificuldade();
        
        obj.setCodDificuldade(Integer.parseInt(txtCod.getText()));
        obj.setQdtDificuldade(Integer.parseInt(txtQtdDificuldade.getText()));
        obj.setTextoDifuculdade(txtTexto.getText());
        
        
        
        NvDificuldadeDAO dao = new NvDificuldadeDAO();
        dao.cadastrarNvDificuldade(obj);
    }//GEN-LAST:event_btnSalvarActionPerformed

